package org.putput.contacts;

public class CreateContact {
  String firstName;
  String lastName;
  String email;
  String mobilePhone;

  public String getFirstName() {
    return firstName;
  }

  public String getLastName() {
    return lastName;
  }

  public String getEmail() {
    return email;
  }

  public String getMobilePhone() {
    return mobilePhone;
  }
}
